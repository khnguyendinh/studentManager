package com.lp.studentManager.studentManager.Entity;

import javax.persistence.*;

@Entity
@Table(name = "student", catalog = "test")
public class StudentBO {
    private int id;
    private String name;
    private int age;

    public StudentBO() {
    }


    @Id
    @Column(name = "ID")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Basic
    @Column(name = "age")
    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public StudentBO(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        StudentBO studentBO = (StudentBO) o;

        if (id != studentBO.id) return false;
        if (age != studentBO.age) return false;
        if (name != null ? !name.equals(studentBO.name) : studentBO.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        result = 31 * result + age;
        return result;
    }
}
